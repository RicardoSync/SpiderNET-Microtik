    public void listarMicrotiks(){
        // Declaración de las variables
        Statement st = null;
        ResultSet rs = null;
        Conexion conexion = new Conexion(); // Obtener la conexión
        Connection con = conexion.getConnection(); // Obtener el objeto Connection

                
        String sql = "SELECT id, nombre, ip, username, password FROM credenciales_microtik";
        
        try {
            DefaultTableModel modelo;
            st = con.createStatement();
            // Ejecutar la consulta y obtener el resultado
            rs = st.executeQuery(sql);
            Object[] microtiks = new Object[5];
            modelo = (DefaultTableModel)tablaMicrotiks.getModel();
            modelo.setRowCount(0);

            while (rs.next()){
                microtiks[0] = rs.getInt("id");
                microtiks[1] = rs.getString("nombre");
                microtiks[2] = rs.getString("ip");
                microtiks[3] = rs.getString("username");
                microtiks[4] = rs.getString("password");
                modelo.addRow(microtiks);
            }
            tablaMicrotiks.setModel(modelo);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al listar los clientes, modulo CrearMicrotik: " + e, "Modulo Microtik", JOptionPane.ERROR_MESSAGE);
        }
        finally{
            // Cerrar el ResultSet, Statement y Connection
            try {
                if (rs != null) rs.close();
                if (st != null) st.close();
                conexion.closeConnection();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error al cerrar la conexión: " + e, "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    
    public void guardarMicrotik(){
        String nombre = entryNombreMicrotik.getText();
        String username = entryUsername.getText();
        String password = entryPassword.getText();
        String ip = entryIp.getText();
        
        InsertarDatos insertarDatos = new InsertarDatos();
        
        insertarDatos.insertarDatos(nombre, ip, username, password);
    }

