    public void iniciarTabla(){
        Conexion conexion = new Conexion();
        Connection cn = conexion.getConnection();
        if (cn != null) {
            PreparedStatement cursor;
            ResultSet resultado;
            String sql = "SELECT id, descripcion, monto, fecha_egreso, metodo_pago FROM egresos";
            try {
                DefaultTableModel modelo;
                cursor = cn.prepareStatement(sql);
                resultado = cursor.executeQuery();

                Object[] usuarios = new Object[5];
                modelo = (DefaultTableModel) jTable1.getModel();
                modelo.setRowCount(0);
                while (resultado.next()) {
                    usuarios[0] = resultado.getInt("id");
                    usuarios[1] = resultado.getString("descripcion");
                    usuarios[2] = resultado.getString("monto");
                    usuarios[3] = resultado.getString("fecha_egreso");
                    usuarios[4] = resultado.getString("metodo_pago");
                    modelo.addRow(usuarios);
                }
                jTable1.setModel(modelo);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Tenemos un error en base de datos: " + e);
            }
        }        
    }
    public void filtradoFecha() {
        int mes = Integer.parseInt((String) comboMes.getSelectedItem());
        int annio = Integer.parseInt(entryAnnio.getText());
        Conexion conexion = new Conexion();
        Connection cn = conexion.getConnection();

        if (cn != null) {
            PreparedStatement cursor;
            ResultSet resultado;
            String sql = "SELECT id, descripcion, monto, fecha_egreso, metodo_pago FROM egresos WHERE month(fecha_egreso) = ? AND year(fecha_egreso) = ?";

            try {
                DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
                modelo.setRowCount(0); // Limpiar la tabla antes de insertar nuevos datos

                cursor = cn.prepareStatement(sql);
                cursor.setInt(1, mes);
                cursor.setInt(2, annio);

                resultado = cursor.executeQuery();
                boolean hayResultados = false;

                while (resultado.next()) {
                    hayResultados = true;
                    Object[] cliente = new Object[5];
                    cliente[0] = resultado.getString("id");
                    cliente[1] = resultado.getString("descripcion");
                    cliente[2] = resultado.getString("monto");
                    cliente[3] = resultado.getString("fecha_egreso");
                    cliente[4] = resultado.getString("metodo_pago");
                    modelo.addRow(cliente);
                }

                jTable1.setModel(modelo);

                // Si no hay resultados, mostrar una notificaciÃ³n
                if (!hayResultados) {
                    JOptionPane.showMessageDialog(null, "No hay pagos registrados para " + mes + "-" + annio, "SpiderNET", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error al filtrar los pagos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
